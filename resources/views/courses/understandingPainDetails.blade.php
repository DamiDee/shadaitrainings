                        <h4 class="title_top">Guidelines for teaching this lesson</h4>
                        <div class="content">
                            <p><b>Lesson Overview</b></p> <br> <br>
                            <p>This one-hour lesson plan is about pain and how your workers should respond to and
                            care for patients with
                            pain. You may use it in
                            a group setting or for
                            individual self-study.
                            Every learner should
                            read the packet, do the
