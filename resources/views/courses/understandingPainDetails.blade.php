                            of achievement. Copy
                            the learner guide, test,
                            and certificate for each
                            learner.</p>
                            <p><b>Learning objectives:</b></p>
                            At the conclusion of this session, participants will:
                            1. Recognize the right to pain management <br>
                            2. Understand pain as the fifth vital sign <br>
                            3. Pay attention to patients’ reports of pain and recognize nonverbal signs <br>
                            4. Know about different kinds of pain <br>
                            5. Know basic pain management concepts.
                        </div>

                        <h4 class="title">Learning Guide</h4>
                        <div class="content">
                        <p><b>Introductory activity: What’s wrong here?:</b></p>
                        <ol> <li>Ask your learners to read each of the conversations from
“What’s wrong here?” As each one is read aloud, ask the group if
anyone can tell you what is wrong with the conversation. See if
they have any ideas about how the conversation should be
handled. Don’t give them any answers or clarifications at this
time; just have them share their ideas. Explain that we will learn
more about pain in this lesson.</li>
                        </ol>
                        
                        </div>

                        <h4 class="title">Lesson activities: </h4>
                        <div class="content">
                            <ol>
                                <li>Explain to your workers that many people misunderstand pain
                                    and how we should respond to it. Ask them to look at the list
of common misconceptions. Tell them that all of these things
are incorrect. Refer them back to the conversations in “What’s
wrong here?” and ask them to identify the misconceptions in
those conversations. Discuss this and allow for questions.
The answers are on the test key.</li>
                                <li>Ask whether any of your workers have ever experienced an
illness that caused them pain. Did they find out that
sometimes people or doctors didn’t believe they were having
pain, or didn’t think there was anything wrong? Emphasize
that all complaints of pain should be investigated, and that
only the patient knows the type and amount of pain he or she
has. Review the material in the learning guide, “Pain: The
Fifth Vital Sign.” Review the nonverbal symptoms of pain and
the importance of reporting a patient’s pain to a supervisor.</li>
                                <li>Help your workers review the types of pain. You may want to
have them read it aloud. Discuss any policies and procedures
you have in your agency about reporting pain, applying warm
or cold compresses, exercise, or massage. Emphasize the
importance of support, which is something all caregivers can
give. Allow time for discussion and questions.</li>

                            </ol>
                        </div>
                         <h4 class="title">Evaluation and feedback</h4>
                        <div class="content">
                        <p>Have the learners take the test. You may grade it together
and discuss any wrong answers. They should receive at least
nine correct answers to pass. Hand out the certificates to learners
who pass the test. Be sure the attendees sign your attendance
roster.</p>
                    
